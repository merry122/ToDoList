package miniProject;

/**
 * Enum representing task status.
 *
 * This enum defines the possible statuses for tasks.
 * - `PENDING`: Indicates the task is yet to be started.
 * - `IN_PROGRESS`: Indicates the task is currently being worked on.
 * - `COMPLETED`: Indicates the task has been finished.
 *
 * Author: Meriem Harrouz
 */
public enum Status {

    PENDING,
    IN_PROGRESS,
    COMPLETED

}