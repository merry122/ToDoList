package miniProject;
/**
 * Priority - Enum representing the priority levels for tasks.
 *
 * The priority levels are defined as follows:
 * - HIGH: Indicates tasks that need immediate attention.
 * - MEDIUM: Indicates tasks that are important but not urgent.
 * - LOW: Indicates tasks that have less urgency and can be addressed later.
 *
 * Author: Harrouz Meriem
 */
public enum Priority {
    HIGHT,
    MEDIUM,
    LOW;
}